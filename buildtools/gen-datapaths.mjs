/**
 * Generate static data-path artifacts from a path.ux DataAPI:
 *   - generated/api-paths.json   machine-readable catalog (source for the ESLint rule)
 *   - generated/API_PATHS.md     human/LLM-readable reference, grouped by top-level struct
 *   - generated/datapaths.ts     KnownDataPath string-literal union (compile-time typo check)
 *
 * Usage:
 *   node buildtools/gen-datapaths.mjs [factoryModule] [exportName] [--out dir]
 *
 * Defaults: factoryModule=example/api/api_define.js exportName=defineAPI out=generated
 *
 * The factory module is bundled with esbuild (platform=node) and its export is
 * invoked to obtain a DataAPI. A DataAPI instance may also be exported directly.
 */
import { writeFile, mkdir, rm } from "node:fs/promises";
import { existsSync } from "node:fs";
import { tmpdir } from "node:os";
import { join, resolve, dirname, relative } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { walkAPI, normalizePath, collectStructCatalog } from "./datapath-walker.mjs";
import { resolveStructImports } from "./resolve-struct-imports.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");

// ---------------------------------------------------------------- renderers

export function renderJSON(entries) {
  const paths = {};
  for (const e of entries) {
    const { path, ...rest } = e;
    paths[path] = rest;
  }
  return JSON.stringify({ version: 1, paths }, null, 2) + "\n";
}

function metaSummary(e) {
  const bits = [];
  if (e.range) bits.push(`range ${e.range[0]}..${e.range[1]}`);
  if (e.unit) bits.push(`unit ${e.unit}`);
  if (e.enumItems) bits.push(`enum {${e.enumItems.join(", ")}}`);
  if (e.dynamic) bits.push("dynamic");
  return bits.join("; ");
}

export function renderMarkdown(entries) {
  const groups = new Map();
  for (const e of entries) {
    const top = e.path.split(/[.[]/)[0];
    if (!groups.has(top)) groups.set(top, []);
    groups.get(top).push(e);
  }

  let md = "# Data API paths\n\n";
  md += "_Auto-generated by `buildtools/gen-datapaths.mjs` — do not edit._\n\n";
  md += "These are the valid `path` strings for `container.prop(...)`, related widget\n";
  md += 'methods, and `<prop path="...">` xmlpage tags. `[n]` marks a list index.\n\n';

  for (const top of [...groups.keys()].sort()) {
    md += `## ${top}\n\n`;
    md += "| Path | Kind | Type | UI name | Notes |\n";
    md += "| --- | --- | --- | --- | --- |\n";
    for (const e of groups.get(top)) {
      const cells = ["`" + e.path + "`", e.kind, e.propType ?? "", e.uiname ?? "", metaSummary(e)];
      md += "| " + cells.map((c) => c.replace(/\|/g, "\\|")).join(" | ") + " |\n";
    }
    md += "\n";
  }
  return md;
}

export function renderDts(entries, moduleName = "path.ux", widgetTags = []) {
  // Only non-indexed paths can be exact string literals; indexed paths
  // (foo[0].bar) fall through to `string` at the call site and are covered by
  // the ESLint rule + runtime diagnostics instead.
  const literals = [
    ...new Set(entries.filter((e) => !e.indexed && !e.path.includes("[n]")).map((e) => e.path)),
  ].sort();

  let ts = "// AUTO-GENERATED by buildtools/gen-datapaths.mjs — do not edit.\n";
  ts += "// Run `npm run gen:paths` to regenerate.\n";
  ts += "// Include this file in your tsconfig to get typo-checking + autocomplete on\n";
  ts += "// container.prop(...) / <prop path=...> (DataPathRegistry) and custom JSX\n";
  ts += "// widget tags (WidgetTagRegistry).\n\n";

  // Convenience standalone union.
  if (literals.length) {
    ts += "export type GeneratedDataPath =\n";
    ts += literals.map((p) => `  | ${JSON.stringify(p)}`).join("\n");
    ts += ";\n\n";
  } else {
    ts += "export type GeneratedDataPath = string;\n\n";
  }

  // Augment the library's registries so container.prop() / JSX accept these.
  if (literals.length || widgetTags.length) {
    ts += `declare module ${JSON.stringify(moduleName)} {\n`;
    if (literals.length) {
      ts += "  interface DataPathRegistry {\n";
      ts += literals.map((p) => `    ${JSON.stringify(p)}: true;`).join("\n");
      ts += "\n  }\n";
    }
    if (widgetTags.length) {
      ts += "  interface WidgetTagRegistry {\n";
      ts += widgetTags.map((t) => `    ${JSON.stringify(t)}: true;`).join("\n");
      ts += "\n  }\n";
    }
    ts += "}\n";
  }
  return ts;
}

/** Posix-ify + ensure a leading "./" for a relative module specifier. */
function toModuleSpecifier(rel) {
  rel = rel.replace(/\\/g, "/");
  return rel.startsWith(".") ? rel : "./" + rel;
}

/**
 * Emit generated/struct-catalog.ts: the typed backing for `api.updateFrom<T>()`.
 *
 * Joins the runtime struct catalog (per-struct members + valued paths, from the
 * walker) with each mapped class's import specifier (from the static
 * api_define pass) to augment the {@link StructCatalog} seam with real,
 * type-checkable entries. `import type` gives us the actual TS types, so callers
 * key off `updateFrom<BrushSettings>(…)` — never a string name.
 *
 * @param entries     deduped walker entries
 * @param importMap   className -> {specifier, fromFile}   (resolveStructImports)
 * @param genDir      absolute output dir (generated/)
 * @param seamAbsFile absolute path to the module declaring `StructCatalog`
 */
export function renderStructCatalog(entries, importMap, genDir, seamAbsFile) {
  const catalog = collectStructCatalog(entries);
  const classes = [...importMap.keys()].filter((c) => catalog.has(c)).sort();
  if (!classes.length) return null;

  // Group class type-imports by their (genDir-relative) source module.
  const byModule = new Map();
  for (const cls of classes) {
    const { specifier, fromFile } = importMap.get(cls);
    const absTarget = resolve(dirname(fromFile), specifier);
    const spec = toModuleSpecifier(relative(genDir, absTarget));
    if (!byModule.has(spec)) byModule.set(spec, []);
    byModule.get(spec).push(cls);
  }

  const seamSpec = toModuleSpecifier(relative(genDir, seamAbsFile).replace(/\.ts$/, ""));

  const union = (values) =>
    values.length ? values.map((v) => JSON.stringify(v)).join(" | ") : "never";

  let ts = "// AUTO-GENERATED by buildtools/gen-datapaths.mjs — do not edit.\n";
  ts += "// Run `npm run gen:paths` to regenerate.\n";
  ts += "//\n";
  ts += "// Augments the `StructCatalog` seam (controller.ts) so `api.updateFrom<T>()`\n";
  ts += "// is type-checked: `path` restricted to datapaths that resolve to T, `prop`\n";
  ts += "// to T's DataStruct members. The library builds standalone with the empty\n";
  ts += "// seam; include this file (see tsconfig) to opt in.\n//\n";
  ts += "// Indexed paths (`foo[n].bar`) are omitted from `paths` (no exact literal);\n";
  ts += "// use `updateChanged<T>(prop)` or the runtime/ESLint path for those.\n\n";

  for (const spec of [...byModule.keys()].sort()) {
    const names = byModule.get(spec).sort();
    ts += `import type { ${names.join(", ")} } from ${JSON.stringify(spec)};\n`;
  }
  ts += `import type { StructCatalog } from ${JSON.stringify(seamSpec)};\n\n`;

  // Nominal brand: many JS-ported model classes have near-empty TS instance
  // types, so a purely structural `self` match would union members across
  // structurally-identical structs. A per-class phantom discriminant (optional,
  // type-only — never present at runtime) makes each type mutually *un*assignable
  // to the others, so `StructEntryFor<T>` resolves to exactly one entry.
  ts += "// Phantom brands (type-only) to keep structurally-similar structs distinct.\n";
  for (const spec of [...byModule.keys()].sort()) {
    const names = byModule.get(spec).sort();
    ts += `declare module ${JSON.stringify(spec)} {\n`;
    for (const cls of names) {
      ts += `  interface ${cls} { readonly __pathuxStruct?: ${JSON.stringify(cls)}; }\n`;
    }
    ts += "}\n";
  }
  ts += "\n";

  ts += `declare module ${JSON.stringify(seamSpec)} {\n`;
  ts += "  interface StructCatalog {\n";
  for (const cls of classes) {
    const { members, paths } = catalog.get(cls);
    const memberU = union([...members].sort());
    const pathU = union([...paths].filter((p) => !p.includes("[n]")).sort());
    ts += `    ${cls}: { self: ${cls}; members: ${memberU}; paths: ${pathU} };\n`;
  }
  ts += "  }\n";
  ts += "}\n";
  return ts;
}

