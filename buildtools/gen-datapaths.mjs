function collectWidgetTags(UIBase) {
  try {
    const tags = UIBase?.getRegisteredTagNames?.();
    return Array.isArray(tags) ? tags : [];
  } catch {
    return [];
  }
}

async function loadApi(factoryModule, exportName) {
  const esbuild = await import("esbuild");

  // Re-export the factory namespace plus the library's UIBase from a single
  // synthetic entry, so that importing the bundle both yields the DataAPI and
  // exposes the tag names registered as a side effect of the import.
  const factorySpec = "./" + factoryModule.replace(/\\/g, "/");
  const stdinContents =
    `export * as __factory from ${JSON.stringify(factorySpec)};\n` +
    `export { UIBase } from "./scripts/core/ui_base.js";\n`;

  const result = await esbuild.build({
    stdin: {
      contents  : stdinContents,
      resolveDir: repoRoot,
      sourcefile: "gen-datapaths-entry.js",
      loader    : "js",
    },
    bundle  : true,
    write   : false,
    format  : "esm",
    platform: "node",
    target  : "es2022",
    // Native/host-only deps that are require()'d lazily (e.g. electron is only
    // pulled in by getElectron() at runtime) must not be resolved while bundling
    // the factory for static path extraction.
    external: ["electron", "marked", "parse5", "diff"],
    banner  : { js: DOM_STUB_BANNER },
    logLevel: "silent",
  });

  const code = result.outputFiles[0].text;
  const tmp = join(tmpdir(), `pathux-datapaths-${process.pid}-${Date.now()}.mjs`);
  await writeFile(tmp, code, "utf8");

  try {
    const mod = await import(pathToFileURL(tmp).href);
    const factory = mod.__factory ?? {};
    const exported = factory[exportName] ?? factory.default;
    if (exported === undefined) {
      throw new Error(`module ${factoryModule} has no export "${exportName}" (or default)`);
    }
    const api = typeof exported === "function" ? exported() : exported;
    if (!api?.rootContextStruct) {
      throw new Error(
        `export "${exportName}" did not yield a DataAPI with a rootContextStruct ` +
          `(call api.setRoot(...) in your factory)`
      );
    }
    return { api, widgetTags: collectWidgetTags(mod.UIBase) };
  } finally {
    await rm(tmp, { force: true });
  }
}

// ---------------------------------------------------------------------- main

async function main(argv) {
  let outDir = "generated";
  let moduleName = "path.ux";
  const positional = [];
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--out") {
      outDir = argv[++i] ?? outDir;
    } else if (argv[i] === "--module") {
      moduleName = argv[++i] ?? moduleName;
    } else {
      positional.push(argv[i]);
    }
  }
  const factoryModule = positional[0] ?? "example/api/api_define.js";
  const exportName = positional[1] ?? "defineAPI";

  let api;
  let widgetTags;
  try {
    ({ api, widgetTags } = await loadApi(factoryModule, exportName));
  } catch (err) {
    console.error(`[gen-datapaths] failed to load ${factoryModule}:`);
    console.error("  " + (err?.message ?? err));
    console.error(
      "  Pass a node-safe factory: node buildtools/gen-datapaths.mjs <module> <exportName>"
    );
    process.exitCode = 1;
    return;
  }

  const entries = walkAPI(api);
  // de-dup identical paths (a struct reachable two ways yields the same leaf paths)
  const seen = new Set();
  const unique = [];
  for (const e of entries) {
    const key = normalizePath(e.path);
    if (seen.has(key)) continue;
    seen.add(key);
    unique.push(e);
  }

  const absOut = resolve(repoRoot, outDir);
  await mkdir(absOut, { recursive: true });
  await writeFile(join(absOut, "api-paths.json"), renderJSON(unique), "utf8");
  await writeFile(join(absOut, "API_PATHS.md"), renderMarkdown(unique), "utf8");
  await writeFile(join(absOut, "datapaths.ts"), renderDts(unique, moduleName, widgetTags), "utf8");

  // Typed updateFrom<T> catalog: needs the api-define *source* (.ts) for the
  // static import-resolution pass. The runtime factory is bundled (.js); prefer
  // its .ts sibling when present.
  let structCatalogNote = "";
  try {
    const factoryAbs = resolve(repoRoot, factoryModule);
    const tsSibling = factoryAbs.replace(/\.js$/, ".ts");
    const apiDefineFile = existsSync(tsSibling) ? tsSibling : factoryAbs;
    const importMap = resolveStructImports(apiDefineFile);
    const seamAbsFile = resolve(repoRoot, "scripts/path-controller/controller/controller.ts");
    const catalogTs = renderStructCatalog(unique, importMap, absOut, seamAbsFile);
    if (catalogTs) {
      await writeFile(join(absOut, "struct-catalog.ts"), catalogTs, "utf8");
      structCatalogNote = `, struct-catalog.ts (${importMap.size} structs)`;
    }
  } catch (err) {
    console.warn("[gen-datapaths] struct-catalog emit skipped: " + (err?.message ?? err));
  }

  console.log(
    `[gen-datapaths] wrote ${unique.length} paths and ${widgetTags.length} widget tags ` +
      `to ${outDir}/ (api-paths.json, API_PATHS.md, datapaths.ts${structCatalogNote})`
  );
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  main(process.argv.slice(2))
    .catch((err) => {
      console.error("[gen-datapaths]", err?.stack ?? err);
      process.exitCode = 1;
    })
    .finally(() => {
      // The bundled app may register timers/animation frames that keep the
      // event loop alive; exit explicitly once artifacts are written.
      process.exit(process.exitCode ?? 0);
    });
}
